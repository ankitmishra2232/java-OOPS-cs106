package com.Ankit.cs106.ex13_ecomSys;

public class Category {
	private String name;
	
	public Category() {
		this.name = "";
	}

	public Category(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
