
public class Course {
	String code;
	String courseName;
	Integer credits;
	Double marks;
	
	
	public Course() {}


	public Course(String code, String courseName, Integer credits, Double marks) {
		
		this.code = code;
		this.courseName = courseName;
		this.credits = credits;
		this.marks = marks;
	}

	
	


}
